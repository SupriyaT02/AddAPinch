                <h2>Paneer butter masala</h2>
              <p><b>Ingredients:</b>Paneer,Onion,Tomatoes,Cashews,Spices (cinnamon,bay leadf,cloves,garam masala,kashmiri red chili poweder,cardamom,butter and cream).</p>
              <ol type="1">
                <li>Heat 1 teaspoon of oil in a pan on medium heat. Once the oil is hot, add the bay leaf, cinnamon stick, cloves and saute for few seconds.Then add the the onion, garlic, ginger and saute for 2 to 3 minutes until the onion is translucent.</li>
                <li>Add the tomatoes and cashews and mix.</li>
                <li>Then add 1 cup of water.</li>
                <li>Cover the pan and cook on medium heat for 15 minutes.</li>
                <li>After 15 minutes, remove the pan from heat. Remove the bay leaf, cinnamon stick and cloves.</li>
                <li>Let the mixture cool down a bit and then transfer to a blender. Its important to let it cool down a bit else it will all blow up from the mixer.</li>
                <li>Grind the masala to a smooth paste and set aside,the same pan now add 2 tablespoons butter along with remaining 1 teaspoon oil on medium heat.</li>
                <li>Once the butter melts, add the red chili powder and the Kashmiri red chili powder and fry for few seconds. This will give the curry a nice red color.</li>
                <li>Then add the ground paste back into the pan along with the garam masala (start with 1/2 teaspoon and add the remaining 1/4 teaspoon at the end only if you feel like the curry needs that extra bit of garam masala), cardamom powder, sugar, salt and tomato paste if using.</li>
                <li>Mix well and cook for 1-2 minutes.Then add the cream and mix</li>
                <li>Add in the paneer and cook for 2 to 3 minutes on medium heat. Finally add crushed kasuri methi.</li>
              </ol>
 
