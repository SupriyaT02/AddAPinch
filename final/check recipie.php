                  <h2>Mutton biriyani</h2>
                <p><b>Ingredients:</b>anise, bay leaves, cardamom, cinnamon, cloves, jaiphal, and javitri along with chillies, rose water, kewda and saffron cooked with succulent mutton pieces.</p>
                <ol type="1">
                  <li>Wash the basmati rice at least thrice to get rid of starch and any impurities</li>
                  <li>Soak the rice in water for at least half an hour so that it cooks faster.Rice for biryani should always be par-cooked to 70% doneness. The rest of the cooking happens in the pot with layers of mutton and rice so that the rice can take on the aroma and flavours. If you cook the rice fully beforehand the end result will be mushy and over cooked rice
                    </li>
                  <li>Make the rice after making the mutton masala because it should be hot when it's layered with the mutton masala.</li>
                  <li>The meat is cut into 1.5-2 inch pieces. It's important to get bone-in pieces as they remain juicier, and the marrow in the bones is delicious. You can use either goat or lamb meat for this recipe.</li>
                  <li>This is a really simple 3 ingredient marinade. It's made with yoghurt, turmeric powder, and salt. This marinade acts more as a tenderizer of the mutton pieces than as a flavouring agent. Make sure you pat dry the washed mutton pieces before putting them in the marinade! In this case we only marinate it for as long as it takes to cook the masala. So 20-30 minutes is sufficient.</li>
                  <li>This mutton masala packs tonnes of flavour because it's got everything from roasted spices to fried onions to oodles of ghee! What I love about this masala is that it's made with pantry staples, so I can literally make biryani any day I want without stressing out about ingredients.</li>
                  <li>Layered biryani is typical of most Hyderabadi biryanis. Use a heavy bottomed pan for this so that the bottom and edges don’t burn.

                    Heres the order in which to make the layers:
                    
                    Mutton masala (or Mutton Curry)
                    Fried onions
                    Mint and coriander leaves
                    Par cooked and flavoured basmati rice
                    Saffron milk mixture
                    Ghee</li>
                  <li>Once the biryani is layered in this order, let it cook on low heat for another 20 minutes. This is where all the flavours come together and the rice gets fully cooked.</li>

                </ol>
