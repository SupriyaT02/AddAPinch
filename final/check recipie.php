                  <h2>Choco Truffle cake</h2>
                <p><b>Ingredients:</b>milk,butter,8 ounces semisweet chocolate, chopped,eggs,vanilla extract,all-purpose flour,sugar,baking soda,saltFilling(6 tablespoons butter,4 ounces bittersweet chocolate, chopped,2-1/2 cups confectioners' sugar,1/2 cup heavy whipping cream)</p>
                <ol type="1">
                  <li>In a large saucepan, cook milk, butter and chocolate over low heat until melted. Remove from the heat; let stand for 10 minutes.</li>
                  <li>Preheat oven to 325°. In a large bowl, beat eggs and vanilla; stir in chocolate mixture until smooth. Combine flour, sugar, baking soda and salt; gradually add to chocolate mixture and mix well (batter will be thin).
                  </li>
                  <li>Transfer to 3 greased and floured 9-in. round baking pans. Bake until a toothpick inserted in center comes out clean, 25-30 minutes. Cool 10 minutes before removing from pans to wire racks to cool completely.</li>
                  <li>For filling, in a small saucepan, melt butter and chocolate. Stir in confectioners' sugar and heavy whipping cream until smooth.</li>
                  <li>For ganache, place chocolate in a small bowl. In a small saucepan, bring cream just to a boil. Pour over chocolate; whisk until smooth. Cool, stirring occasionally, until ganache reaches a spreading consistency.</li>
                  <li>Place 1 cake layer on a serving plate; spread with half of the filling. Repeat layers. Top with remaining cake layer. Spread ganache over top and side of cake. Store in the refrigerator.</li>
